    public void getRecords(){
        try{
           Connection  con = DBConnection.getMysqlConnection();
           PreparedStatement pst = con.prepareStatement("select * from fees_details order by reciept_no desc limit 1");
           ResultSet rs = pst.executeQuery();
           rs.next();
           recieptNo.setText(rs.getString("reciept_no"));
           paymentMode.setText(rs.getString("payment_mode"));
           
           String payment = rs.getString("payment_mode");
           
           if(payment.equalsIgnoreCase("cheque")){
               lbl_cheque_dd.setText("Cheque No :");
               cheque_dd_no.setText(rs.getString("cheque_no"));
               bankName.setText(rs.getString("bank_Name"));
           }
           
           if(payment.equalsIgnoreCase("card")){
               lbl_cheque_dd.setText("Card Number");
               cheque_dd_no.setText("Not Required");
               bankName.setText(rs.getString("bank_name"));
           }
           
           if(payment.equalsIgnoreCase("dd")){
               lbl_cheque_dd.setText("DD No :");
               cheque_dd_no.setText(rs.getString("dd_no"));
               bankName.setText(rs.getString("bank_name")); 
           }
           
           if(payment.equalsIgnoreCase("cash")){
               lbl_cheque_dd.setText(" ");
               cheque_dd_no.setText(" ");
               bankName.setText("NA"); 
           }
           
           recievedFrom.setText(rs.getString("student_name"));
           txtDate.setText(rs.getString("date"));
           gstinNo.setText(rs.getString("gstin"));
           year1.setText(rs.getString("year1"));
           year2.setText(rs.getString("year2"));
           
           srNo.setText(rs.getString("reciept_no"));
           
           head.setText(rs.getString("course_name"));
           txtRs.setText(rs.getString("amount"));
           cgst.setText(rs.getString("cgst"));
           sgst.setText(rs.getString("sgst"));
           totalRs.setText(rs.getString("total_amount"));
           rsInWords.setText(rs.getString("Total_in_words"));
           
        }
        catch(Exception e){
            e.printStackTrace();
        }
    }

