    private void btn_l1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_l1MouseClicked
        
        PrinterJob job = PrinterJob.getPrinterJob();
        job.setJobName("Print Data");
        
        job.setPrintable(new Printable(){
            
            @Override
            public int print(Graphics pg, PageFormat pf, int pageNum){
                pf.setOrientation(PageFormat.LANDSCAPE);
                if(pageNum>0){
                    return Printable.NO_SUCH_PAGE;
                }
                
                Graphics2D q2 = (Graphics2D)pg;
                q2.translate(pf.getImageableX(), pf.getImageableY());
                q2.scale(0.47, 0.47);
                
                print_panel.print(q2);
                
                return Printable.PAGE_EXISTS;
            }
        
        
            });
        boolean ok = job.printDialog();
        if(ok){
            try
            {
               job.print();
            }
            catch(PrinterException ex)
            {
                ex.printStackTrace();
            }
        }
        
        
    }//GEN-LAST:event_btn_l1MouseClicked

