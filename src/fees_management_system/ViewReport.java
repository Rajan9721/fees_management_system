    private void btnPrintActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPrintActionPerformed
        SimpleDateFormat dateFormat = new SimpleDateFormat("YYYY-MM-dd");
        String dateFrom = dateFormat.format(fromDate.getDate());
        String dateTo = dateFormat.format(toDate.getDate());
        MessageFormat header = new MessageFormat("Report From"+dateFrom+" to "+dateTo);
        MessageFormat footer = new MessageFormat("Page(0,number,integer)");
        
        try{
            tblViewReport.print(JTable.PrintMode.FIT_WIDTH, header, footer);
        }
        catch(Exception e){
            e.getMessage();
        }
        
    }//GEN-LAST:event_btnPrintActionPerformed

    private void btnBrowseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBrowseActionPerformed
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.showOpenDialog(this);
        SimpleDateFormat dateFormat = new SimpleDateFormat("YYYY-MM-dd");
        String date = dateFormat.format(new Date());
        try{
            File f = fileChooser.getSelectedFile();
            String path = f.getAbsolutePath();
            
            path = path+" "+date+".xlsx";
            txtFilePath.setText(path);
        }
        catch(Exception e){
            e.getMessage();
        }
    }//GEN-LAST:event_btnBrowseActionPerformed

