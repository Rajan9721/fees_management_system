    public void fillCourse(){
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/fees_management","root", "0170");
            PreparedStatement pst = con.prepareStatement("Select cname from course");
            ResultSet rs = pst.executeQuery();
            
            while(rs.next()){
                comboCourse.addItem(rs.getString("cname"));
            }
        }catch(Exception e){
            e.printStackTrace();
        }
    }
    
    public void setRecordToTable(){
        
        Float totalAmount = 0f;
        
        String cname = comboCourse.getSelectedItem().toString();
        SimpleDateFormat dateFormat = new SimpleDateFormat("YYYY-MM-dd");
        String dateFrom = dateFormat.format(fromDate.getDate());
        String dateTo = dateFormat.format(toDate.getDate());
        
        
        
        try{
            Connection con = DBConnection.getMysqlConnection();
            PreparedStatement pst = con.prepareStatement("Select * from fees_details where date between ? and ? and course_name = ? ");
            
            pst.setString(1, dateFrom);
            pst.setString(2, dateTo);
            pst.setString(3, cname);
            
            ResultSet rs = pst.executeQuery();
            
            while(rs.next()){
                String recieptNo = rs.getString("reciept_no");
                String rollNo = rs.getString("roll_no");
                String studentName = rs.getString("student_name");
                String courseName = rs.getString("course_name");
                float amount = rs.getFloat("total_amount");
//                String amount = rs.getString("total_amount");
//                String date = rs.getDate("date").toString();
                String remark = rs.getString("remarks");
                
                totalAmount = totalAmount + amount;
                
                Object[] obj = {recieptNo, rollNo, studentName, courseName, amount,remark};
                
                model = (DefaultTableModel)tblViewReport.getModel();
                model.addRow(obj);
                
                selectedCourse.setText(courseName);
                totalAmountCollect.setText(totalAmount.toString());
                totalAmountInWord.setText(NumberToWordsConverter.convert(totalAmount.intValue()));
            }
        }
        catch(Exception e){
            e.printStackTrace();
        }
    }

    public void clearTable(){
        DefaultTableModel model = (DefaultTableModel)tblViewReport.getModel();
        model.setRowCount(0);
    }
